DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM public.users
        WHERE email IS NOT NULL
        GROUP BY email
        HAVING COUNT(*) > 1
    ) THEN
        CREATE UNIQUE INDEX IF NOT EXISTS uk_users_email ON public.users(email);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM public.refugios
        WHERE nombre IS NOT NULL
        GROUP BY nombre
        HAVING COUNT(*) > 1
    ) THEN
        CREATE UNIQUE INDEX IF NOT EXISTS uk_refugios_nombre ON public.refugios(nombre);
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_mascotas_refugios') THEN
        ALTER TABLE public.mascotas
            ADD CONSTRAINT fk_mascotas_refugios FOREIGN KEY (refugio_id) REFERENCES public.refugios(id) NOT VALID;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_solicitudes_adopcion_users') THEN
        ALTER TABLE public.solicitudes_adopcion
            ADD CONSTRAINT fk_solicitudes_adopcion_users FOREIGN KEY (usuario_id) REFERENCES public.users(id) ON DELETE CASCADE NOT VALID;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_solicitudes_adopcion_mascotas') THEN
        ALTER TABLE public.solicitudes_adopcion
            ADD CONSTRAINT fk_solicitudes_adopcion_mascotas FOREIGN KEY (mascota_id) REFERENCES public.mascotas(id) ON DELETE CASCADE NOT VALID;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_solicitudes_ingreso_users') THEN
        ALTER TABLE public.solicitudes_ingreso_mascota
            ADD CONSTRAINT fk_solicitudes_ingreso_users FOREIGN KEY (usuario_id) REFERENCES public.users(id) ON DELETE CASCADE NOT VALID;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_solicitudes_ingreso_refugios') THEN
        ALTER TABLE public.solicitudes_ingreso_mascota
            ADD CONSTRAINT fk_solicitudes_ingreso_refugios FOREIGN KEY (refugio_id) REFERENCES public.refugios(id) NOT VALID;
    END IF;
END $$;
