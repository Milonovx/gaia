CREATE SCHEMA IF NOT EXISTS public;

DO $$
BEGIN
    IF to_regclass('public.mascotas') IS NULL AND to_regclass('public.mascota') IS NOT NULL THEN
        ALTER TABLE public.mascota RENAME TO mascotas;
    ELSIF to_regclass('public.mascotas') IS NULL AND to_regclass('public.tbl_mascotas') IS NOT NULL THEN
        ALTER TABLE public.tbl_mascotas RENAME TO mascotas;
    ELSIF to_regclass('public.mascotas') IS NULL AND to_regclass('public."Mascota"') IS NOT NULL THEN
        ALTER TABLE public."Mascota" RENAME TO mascotas;
    END IF;

    IF to_regclass('public.refugios') IS NULL AND to_regclass('public.refugio') IS NOT NULL THEN
        ALTER TABLE public.refugio RENAME TO refugios;
    ELSIF to_regclass('public.refugios') IS NULL AND to_regclass('public.tbl_refugios') IS NOT NULL THEN
        ALTER TABLE public.tbl_refugios RENAME TO refugios;
    ELSIF to_regclass('public.refugios') IS NULL AND to_regclass('public."Refugio"') IS NOT NULL THEN
        ALTER TABLE public."Refugio" RENAME TO refugios;
    END IF;

    IF to_regclass('public.users') IS NULL AND to_regclass('public."user"') IS NOT NULL THEN
        ALTER TABLE public."user" RENAME TO users;
    ELSIF to_regclass('public.users') IS NULL AND to_regclass('public.usuario') IS NOT NULL THEN
        ALTER TABLE public.usuario RENAME TO users;
    ELSIF to_regclass('public.users') IS NULL AND to_regclass('public.usuarios') IS NOT NULL THEN
        ALTER TABLE public.usuarios RENAME TO users;
    END IF;

    IF to_regclass('public.solicitudes_adopcion') IS NULL AND to_regclass('public.solicitud_adopcion') IS NOT NULL THEN
        ALTER TABLE public.solicitud_adopcion RENAME TO solicitudes_adopcion;
    END IF;

    IF to_regclass('public.solicitudes_ingreso_mascota') IS NULL AND to_regclass('public.solicitud_ingreso_mascota') IS NOT NULL THEN
        ALTER TABLE public.solicitud_ingreso_mascota RENAME TO solicitudes_ingreso_mascota;
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS public.users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(80) NOT NULL,
    apellido VARCHAR(80) NOT NULL,
    email VARCHAR(160) NOT NULL,
    password VARCHAR(255) NOT NULL,
    telefono VARCHAR(30),
    foto_perfil VARCHAR(500),
    role VARCHAR(30) NOT NULL DEFAULT 'ROLE_USER',
    fecha_creacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS public.refugios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(120) NOT NULL,
    direccion VARCHAR(180) NOT NULL,
    telefono VARCHAR(30),
    descripcion TEXT,
    latitud NUMERIC(10,7) NOT NULL,
    longitud NUMERIC(10,7) NOT NULL,
    ciudad VARCHAR(80) NOT NULL
);

CREATE TABLE IF NOT EXISTS public.mascotas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    edad INTEGER NOT NULL,
    tipo_edad VARCHAR(12) NOT NULL DEFAULT 'ANIOS',
    raza VARCHAR(80) NOT NULL,
    tamano VARCHAR(20) NOT NULL,
    genero VARCHAR(20) NOT NULL,
    descripcion TEXT,
    estado_salud VARCHAR(120) NOT NULL,
    vacunado BOOLEAN NOT NULL DEFAULT FALSE,
    esterilizado BOOLEAN NOT NULL DEFAULT FALSE,
    imagen_url VARCHAR(500),
    disponible BOOLEAN NOT NULL DEFAULT TRUE,
    fecha_rescate DATE,
    refugio_id BIGINT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS public.solicitudes_adopcion (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id BIGINT NOT NULL,
    mascota_id BIGINT NOT NULL,
    mensaje TEXT NOT NULL,
    estado VARCHAR(30) NOT NULL DEFAULT 'PENDIENTE',
    fecha_solicitud TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS public.solicitudes_ingreso_mascota (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id BIGINT NOT NULL,
    refugio_id BIGINT NOT NULL,
    nombre_mascota VARCHAR(100) NOT NULL,
    edad INTEGER NOT NULL,
    tipo_edad VARCHAR(12) NOT NULL DEFAULT 'ANIOS',
    raza VARCHAR(80) NOT NULL,
    tamano VARCHAR(20) NOT NULL,
    genero VARCHAR(20) NOT NULL,
    descripcion TEXT NOT NULL,
    estado_salud VARCHAR(120) NOT NULL,
    vacunado BOOLEAN NOT NULL DEFAULT FALSE,
    esterilizado BOOLEAN NOT NULL DEFAULT FALSE,
    motivo_entrega TEXT NOT NULL,
    telefono_contacto VARCHAR(30) NOT NULL,
    direccion VARCHAR(180) NOT NULL,
    imagen_url VARCHAR(500) NOT NULL,
    estado_solicitud VARCHAR(30) NOT NULL DEFAULT 'PENDIENTE',
    fecha_solicitud TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

